    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        if (validateInput()) {
            Angsuran angsuran = new Angsuran()
                    .setJumlah(Long.parseLong(inputJumlah.getText()))
                    .setKeterangan(inputKeterangan.getText())
                    .setAnggota((Anggota) inputAnggota.getSelectedItem())
                    .setPinjaman(new Pinjaman(Integer.parseInt(inputIdPinjaman.getText())));

            angsuranController.setDML(angsuran, CRUD.CREATE);
            JOptionPane.showMessageDialog(this, "Berhasil membayar angsuran pinjaman.");

            resetForm();
            setDataAngsuran(angsuranController.fetchAngsuran());
        }
    }//GEN-LAST:event_btnSimpanActionPerformed

