    private void setDataAnggota(List<Anggota> dataAnggota) {
        if (dataAnggota == null) {
            JOptionPane.showMessageDialog(this, "Data Anggota tidak bisa di load", "Gagal Koneksi",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            inputAnggota.setModel(new DefaultComboBoxModel(dataAnggota.toArray()));
        }
    }

    private void setDataAngsuran(List<Angsuran> dataAngsuran) {
        if (dataAngsuran == null) {
            JOptionPane.showMessageDialog(this, "Data Angsuran tidak bisa di load", "Gagal Koneksi",
                    JOptionPane.ERROR_MESSAGE);
        } else {
            tblModel = new DefaultTableModel();
            tblModel.setColumnIdentifiers(new String[]{"ID", "ID Pinjaman", "Total Pinjaman", "NIK", "Nama Anggota", "Jumlah", "Keterangan", "Tanggal Transaksi"});

            DateTimeFormatter df = DateTimeFormatter.ofPattern("yyyy-MM-dd HH:mm");

            for (Angsuran angsuran : dataAngsuran) {
                Object[] o = new Object[9];
                o[0] = angsuran.getId();
                o[1] = angsuran.getPinjaman().getId();
                o[2] = Helpers.formatRupiah(angsuran.getPinjaman().getTotalPinjaman());
                o[3] = angsuran.getAnggota().getNik();
                o[4] = angsuran.getAnggota().getNama();
                o[5] = Helpers.formatRupiah(angsuran.getJumlah());
                o[6] = angsuran.getKeterangan();
                o[7] = df.format(angsuran.getTanggalTransaksi());
                tblModel.addRow(o);
            }

            tblPinjaman.setAutoResizeMode(javax.swing.JTable.AUTO_RESIZE_ALL_COLUMNS);
            tblPinjaman.setModel(tblModel);
        }
    }

    private void resetForm() {
        inputIdAngsuran.setText("");
        inputIdAngsuran.setEnabled(false);
        inputJumlah.setText("");
        inputKeterangan.setText("");
    }

